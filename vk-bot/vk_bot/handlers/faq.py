async def cmd_faq(message: Message):
    await message.answer("❓ Частые вопросы. Выбери:", keyboard=faq_kb())


async def cmd_faq_item(message: Message, item: str):
    if item not in FAQ:
        await message.answer("Вопрос не найден.", keyboard=faq_kb())
        return
    _, text = FAQ[item]
    await message.answer(text, keyboard=faq_item_kb())
